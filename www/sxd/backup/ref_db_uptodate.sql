#	FU`checkSecurityGroup`utf8_general_ci	;
CREATE FUNCTION `checkSecurityGroup`(
        user_id INTEGER(11),
        creator_id INTEGER(11),
        pub_id INTEGER(11)
    ) RETURNS tinyint(4)
BEGIN
 IF user_id = creator_id THEN
   RETURN 1;
 ELSE
 
 	SELECT COUNT(*) INTO @del FROM `author_publication_rights` apr, `acl_users` u 
    WHERE apr.`id_publication` = pub_id AND apr.`id_author` = u.`author_id` 
    AND u.`id` = user_id;
    
    IF @del > 0 THEN
    	RETURN 1;
    END IF;
    
    SELECT COUNT(*) INTO @coauth FROM `author_publication` ap, `acl_users` u 
    WHERE ap.`id_publication` = pub_id AND ap.`id_author` = u.`author_id` 
    AND u.`id` = user_id;
    
    IF @coauth > 0 THEN
    	RETURN 1;
    END IF;
    
 	SELECT u.`group_id` INTO @creator_group FROM `acl_users` u WHERE u.`id` = creator_id;
    SELECT u.`group_id` INTO @user_group FROM `acl_users` u WHERE u.`id` = user_id;
   WHILE @creator_group != 0 DO
   	
	SELECT gr.`master` INTO @master_ FROM `acl_user_groups` gr
    WHERE gr.`id` = @creator_group; 
    
    IF @user_group = @master_ THEN
    	RETURN 1;
    ELSE
    	SET @creator_group = @master_;
    END IF;
    
   END WHILE;
   RETURN 0;
 END IF;
END	;
#	FU`concat_authors`utf8_general_ci	;
CREATE FUNCTION `concat_authors`(
        ID INTEGER(11)
    ) RETURNS varchar(100) CHARSET cp1251
BEGIN
DECLARE done INTEGER DEFAULT 0;
DECLARE nName VARCHAR(100);
DECLARE res VARCHAR(100) DEFAULT '';
DECLARE vCursor CURSOR FOR SELECT CONCAT(a.`family`,' ',SUBSTRING(a.`name`,1,1),'. ',SUBSTRING(a.`patronymic`,1,1),'.') AS FIO FROM `author` a, `author_publication` a_p, `publication` p
WHERE p.`id` = a_p.`id_publication` AND
a.`id` = a_p.`id_author` AND
p.`id` = ID; 
DECLARE CONTINUE HANDLER FOR SQLSTATE '02000' SET done=1;
OPEN vCursor;
WHILE done = 0 DO
	FETCH vCursor INTO nName;
    SET res = CONCAT(res,'|',nName);
    SET nName = '';   
END WHILE;
CLOSE vCursor;
SET res = TRIM(BOTH '|' FROM res);
RETURN res;
END	;
